def get_start_urls(language_code: str) -> list:
    """Get start URLs for a specific language."""
    return START_URLS.get(language_code, [])


def get_all_start_urls() -> list:
    """Get all start URLs across all languages."""
    all_urls = []
    for urls in START_URLS.values():
        all_urls.extend(urls)
    return all_urls


def get_urls_count_by_language() -> dict:
    """Get count of start URLs per language."""
    return {lang: len(urls) for lang, urls in START_URLS.items()}
